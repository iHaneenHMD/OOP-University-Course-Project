    public boolean isValidTotal(){
        boolean test=false;
       try{
       if(!isValidName()){
           test=true;
           throw new Validation("The name is incorrect , please choose a suitable name");
         }
        if(!isValidID()){
            test=true;
           throw new Validation("The ID is incorrect , please write correct ID");
        }
        }
        catch (Validation e){
           JOptionPane.showMessageDialog(null,e.getMessage(), "Invalid Input",JOptionPane.ERROR_MESSAGE);
      }
       return test;   
    }
    public boolean isValidName(){
       //return true when name not contain number
        //return false if name contain number
       return Pattern.matches("[a-zA-Z]+",jTextField1.getText());
    }
     public boolean isValidID(){
       //return true when ID contain number
        //return false if ID contain charcter
       return Pattern.matches("[0-9]+",jTextField2.getText());
    }
